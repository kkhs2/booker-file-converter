import { h, Fragment } from 'preact';
import { cn } from '../../../../utils/helpers';
import Icons from '../../Icons/Icons';
import { useState } from 'preact/hooks';
import { useClickOutside } from '../../../../utils';

/**
 * Account Control Component
 * @description Renders the user account control component
 * @param {Object} props - Component props
 * @param {Object} props.user - User object containing user details
 * @param {Function} props.onclick - Function to call when the user clicks on the account control
 *
 * @returns {JSX.Element} - The Account Control component
 */

export const AccountControl = ({ user, onclick }) => {
  const [showDropdown, setShowDropdown] = useState(false);

  const dropdownRef = useClickOutside(() => {
    setShowDropdown(false);
  }, showDropdown);

  // Helper function to get the current account safely
  const getCurrentAccount = () => {
    if (
      !user.accounts ||
      !Array.isArray(user.accounts) ||
      user.accounts.length === 0
    ) {
      return null;
    }
    const currentAccount = user.accounts.filter((account) => account.current);
    return currentAccount.length > 0 ? currentAccount[0] : null;
  };

  const currentAccount = getCurrentAccount();

  return (
    <div className="tw:relative">
      {/* dropdown button */}
      <button
        aria-label="User Account"
        onClick={() => {
          if (onclick) onclick();
          setShowDropdown(!showDropdown);
        }}
        className={cn(
          'tw:inline-flex tw:w-full tw:cursor-pointer tw:items-center tw:justify-between tw:rounded-[120px] tw:bg-grey-1100 tw:p-1 tw:pr-4 tw:transition-colors tw:hover:bg-tertiary-500 tw:lg:w-[230px]',

          showDropdown === true && 'tw:!bg-tertiary-500',

          // user account type
          user.accounttype === 'londis' && 'tw:bg-londis-light',
          user.accounttype === 'budgens' && 'tw:bg-budgens-light',
          user.accounttype === 'premier' && 'tw:bg-premier-light',
          user.accounttype === 'shoplocally' && 'tw:bg-shoplocally',
          user.accounttype === 'shopndrive' && 'tw:bg-shopndrive',
          user.accounttype === 'ontradeclub' && 'tw:bg-ontradeclub',
          user.accounttype === 'familyshopper' && 'tw:bg-familyshopper',
          user.accounttype === 'custom' && user.accountclassname,
        )}
      >
        {/* user avatar based on user.accounttype */}
        <div className="tw:flex tw:items-center tw:gap-2">
          <Avatar type={user.accounttype} src={user.accountlogo} />

          {/* user name */}
          <span className="tw:max-w-[200px] tw:overflow-hidden tw:text-lg tw:font-medium tw:text-ellipsis tw:whitespace-nowrap tw:text-black tw:lg:max-w-[150px]">
            {user.name}
          </span>
        </div>

        {/* dropdown icon */}
        <Icons.chevronRight classname="tw:lg:rotate-90 tw:w-4 tw:h-4 tw:flex-shrink-0" />
      </button>

      {/* dropdown menu */}
      {showDropdown && (
        <div
          className="tw:absolute tw:top-[58px] tw:right-0 tw:z-10 tw:max-h-[510px] tw:w-[320px] tw:overflow-auto tw:rounded-lg tw:bg-white tw:py-3"
          style={{
            boxShadow:
              '-1px -1px 4px 4px rgba(0, 0, 0, 0.02), 6px 10px 24px 0px rgba(0, 0, 0, 0.30)',
          }}
          ref={dropdownRef}
        >
          {/* user account details */}
          <div>
            <span className="tw:px-3 tw:text-sm tw:text-grey-900">
              {user.email}
            </span>

            {/* current account */}
            <div className="tw:mt-2 tw:mb-2 tw:flex tw:items-center tw:justify-between tw:border-b tw:border-grey-200 tw:px-3 tw:pb-3">
              <span className="tw:text-base tw:font-bold tw:text-grey-900">
                {currentAccount ? currentAccount.name : 'Account'}
              </span>
              <span className="tw:text-sm tw:font-bold tw:text-grey-900">
                {currentAccount ? currentAccount.id : ''}
              </span>
            </div>

            <a
              href={user.accounturl}
              className="tw:flex tw:w-full tw:cursor-pointer tw:items-center tw:justify-between tw:p-3 tw:text-base tw:hover:bg-grey-200"
            >
              My Account
            </a>

            <button
              className="tw:flex tw:w-full tw:cursor-pointer tw:items-center tw:justify-between tw:p-3 tw:hover:bg-grey-200"
              onClick={() => user.onsignout()}
            >
              <span className="tw:text-base tw:text-black">Sign out</span>
            </button>
          </div>

          {user.accounts && user.accounts.length > 1 && (
            <div>
              {/* user accounts */}

              <div className="tw:mt-4 tw:mb-2 tw:flex tw:items-center tw:justify-between tw:border-b tw:border-grey-200 tw:px-3 tw:pb-3">
                <span className="tw:text-base tw:font-bold tw:text-grey-900">
                  Switch Account
                </span>
              </div>
              <a
                href={user.manageaccounturl}
                className="tw:flex tw:w-full tw:cursor-pointer tw:items-center tw:justify-between tw:p-3 tw:hover:bg-grey-200"
              >
                <span className="tw:text-base tw:text-black">
                  Add or remove accounts
                </span>
              </a>

              {user.accounts.map((account) => (
                <button
                  className="tw:flex tw:w-full tw:cursor-pointer tw:items-center tw:justify-between tw:p-3 tw:hover:bg-grey-200"
                  onClick={() => {
                    user.onswitchaccount(account.id);
                    setShowDropdown(false);
                  }}
                >
                  <span className="tw:text-base tw:text-black">
                    {account.name}
                  </span>
                  <span className="tw:text-sm tw:text-black">{account.id}</span>
                </button>
              ))}
            </div>
          )}
        </div>
      )}
    </div>
  );
};

